public class Rectangle
{
  private int length;
  private int width;
  
  public Rectangle(int Length, int Width)
  {
    length = Length;
    width = Width;
  }
  public int getLength()
  {
    return length;
  }
  public int getWidth()
  {
    return width;
  }
  public toString()
  {
    return "Rectange length is: " +  length + " Rectangle width is: " + width;
    
  }
}
